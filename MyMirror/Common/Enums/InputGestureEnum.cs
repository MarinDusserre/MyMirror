namespace Common.Enums
{
    /// <summary>
    /// Screen input gestures enumerator
    /// </summary>
    public enum InputGestureEnum
    {
        /// <summary>
        /// Click gesture
        /// </summary>
        Click,

        /// <summary>
        /// Positive roll gesture
        /// </summary>
        RollIn,

        /// <summary>
        /// Negative roll gesture
        /// </summary>
        Rollout,

        /// <summary>
        /// Exit gesture
        /// </summary>
        Exit,

        /// <summary>
        /// Simple position Change
        /// </summary>
        Position,

        /// <summary>
        /// No gesture
        /// </summary>
        None
    }
}
